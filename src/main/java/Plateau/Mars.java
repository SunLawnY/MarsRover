package Plateau;

public class Mars {
    private final int xAxis;
    private final int yAxis;

    public Mars(int xAxis, int yAxis) {
        this.xAxis = xAxis;
        this.yAxis = yAxis;
    }

    public int getxAxis() {
        return xAxis;
    }

    public int getyAxis() {
        return yAxis;
    }
}
